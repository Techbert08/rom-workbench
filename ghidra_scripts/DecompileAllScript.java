// Decompile every function in the current program and write the result to a file.
// @category   WPC
// @description Post-script used by analyze.ps1. Takes one argument: the output path.

import java.io.BufferedWriter;
import java.io.FileWriter;
import java.io.IOException;

import ghidra.app.decompiler.DecompInterface;
import ghidra.app.decompiler.DecompileOptions;
import ghidra.app.decompiler.DecompileResults;
import ghidra.app.script.GhidraScript;
import ghidra.program.model.listing.Function;

public class DecompileAllScript extends GhidraScript {

    @Override
    public void run() throws Exception {
        String[] args = getScriptArgs();
        if (args.length < 1) {
            throw new IllegalArgumentException(
                "DecompileAllScript requires one argument: <output-file>");
        }
        String outputPath = args[0];

        // Flush pending analysis on the many functions our discovery scripts
        // created. Without this, cross-references on freshly-disassembled
        // banked code aren't fully wired up and the decompiler bails on
        // anything it can't resolve. Empirically: ~700 extra clean decompiles.
        println("DecompileAllScript: running analyzeChanges() to flush analyses...");
        analyzeChanges(currentProgram);

        DecompInterface decomp = new DecompInterface();
        try {
            DecompileOptions options = new DecompileOptions();
            decomp.setOptions(options);
            decomp.toggleCCode(true);
            decomp.toggleSyntaxTree(true);
            decomp.setSimplificationStyle("decompile");

            if (!decomp.openProgram(currentProgram)) {
                throw new RuntimeException(
                    "DecompInterface.openProgram failed: " + decomp.getLastMessage());
            }

            // Skip functions whose decompiled body contains these markers:
            // they indicate the decompiler aborted on garbage bytes, which
            // happens for the many spurious functions we created by probing
            // (offset, page) combinations where the page actually holds data.
            final String[] noiseMarkers = { "halt_baddata", "Bad instruction" };

            int ok = 0;
            int fail = 0;
            int skipped = 0;
            try (BufferedWriter out = new BufferedWriter(new FileWriter(outputPath))) {
                out.write("// Decompilation of " + currentProgram.getName() + "\n");
                out.write("// Generated by analyze-wpc-rom skill (DecompileAllScript.java)\n");
                out.write("// Filtered: functions containing 'halt_baddata' / 'Bad instruction' omitted.\n\n");

                for (Function func : currentProgram.getFunctionManager().getFunctions(true)) {
                    if (monitor.isCancelled()) {
                        break;
                    }
                    String header = "// --- " + func.getName() + " @ "
                                  + func.getEntryPoint() + " ---\n";
                    try {
                        DecompileResults result = decomp.decompileFunction(func, 60, monitor);
                        if (result != null && result.decompileCompleted()
                                && result.getDecompiledFunction() != null) {
                            String body = result.getDecompiledFunction().getC();
                            if (containsAny(body, noiseMarkers)) {
                                skipped++;
                                continue;
                            }
                            out.write(header);
                            out.write(body);
                            out.write("\n");
                            ok++;
                        } else {
                            // Failed decompiles are usually also noise from our
                            // overgenerated functions; drop them.
                            fail++;
                        }
                    } catch (Exception e) {
                        fail++;
                    }
                }

                out.write(String.format(
                    "// Done. ok=%d skipped=%d fail=%d total=%d%n",
                    ok, skipped, fail, ok + skipped + fail));
            }
            println("DecompileAllScript: wrote " + outputPath
                    + " (ok=" + ok + " skipped=" + skipped + " fail=" + fail + ")");
        } finally {
            decomp.dispose();
        }
    }

    private static boolean containsAny(String haystack, String[] needles) {
        for (String n : needles) {
            if (haystack.contains(n)) return true;
        }
        return false;
    }
}
